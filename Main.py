class Main():
    # ======================================================================================
    # 전역상수 관리 - 필수영역(정적변수)
    # ======================================================================================
    # ——————————————————————————————————————————————————————————————————————————————
    # Const         - 전역상수 클래스 설명
    # Inheritance   - None
    # ——————————————————————————————————————————————————————————————————————————————
    class Const():
        CONST_NAME = 0                      # 전역상수 선언 예
    # ======================================================================================
    # 전역클래스 관리 - 필수영역(사용자정의 보조클래스)
    # ======================================================================================
    # ——————————————————————————————————————————————————————————————————————————————
    # SubClassName  - 보조 클래스 설명
    # Inheritance   - None
    # ——————————————————————————————————————————————————————————————————————————————
    class SubClassName():
        pass                                # 사용자정의 클래스 선언 예
    # ======================================================================================
    # 전역변수 관리 - 필수영역(정적변수)
    # ======================================================================================
    Hello = None                            # 전역변수 선언 예
    # ======================================================================================
    # 생성자 관리 - 필수영역(인스턴스함수)
    # ======================================================================================
    # ——————————————————————————————————————————————————————————————————————————————
    # __init__  - 생성자(인스턴스변수 생성/초기화)
    # Args      - self : Main 객체 인스턴스
    # Return    - None
    # ——————————————————————————————————————————————————————————————————————————————
    def __init__(self) -> None:
        try:
            # ==============================================================================
            # 전역변수 관리 - 필수영역(인스턴스변수)
            # ==============================================================================
            self.Form = webMain.Form()              # Streamlit 웹 폼 객체 선언
            # ==============================================================================
            # 부모객체 생성 - 필수영역(상속을 받은 경우 필수)
            # ==============================================================================
            # super().__init__()                      # 부모 클래스가 하나인 경우 부모 객체 생성
            # Parent1.__init__(self)                  # 부모 클래스가 두개 이상인 경우 부모 객체 생성
            # Parent2.__init__(self)                  # 부모 클래스가 두개 이상인 경우 부모 객체 생성
            # ==============================================================================
        except Exception as Ex:
            print(traceback.format_exc())           # 예외 출력하기(예외처리)
    # ======================================================================================
    # 프로그램 진입점 관리 - 필수영역
    # ======================================================================================
    # ——————————————————————————————————————————————————————————————————————————————
    # Start     - 응용 프로그램 진입점(Entry Point)
    # Args      - self : Main 객체 인스턴스
    # Return    - None
    # ——————————————————————————————————————————————————————————————————————————————
    def Start(self) -> None:
        try:
            # --------------------------------------------------------------
            # 모듈 수정 후 디버깅시 반경될 수 있도록 재로드(임시)
            # --------------------------------------------------------------
            import importlib                        # importlib 모듈(Built-in)
            importlib.reload(Css)
            importlib.reload(webMain)
            # --------------------------------------------------------------
            st.set_page_config(layout="wide")
            # --------------------------------------------------------------
            # CSS 환경 설정(.st-key 다음이 key로 사용 됨)
            # --------------------------------------------------------------
            st.html(Css.ExpMainStyle)                   # Main Expander 용 CSS
            st.html(Css.ContTabStyle)                   # Tab  Container 용 CSS
            st.html(Css.ContGugudanScrollStyle)         # 구구단 Container Scroll 용 CSS
            st.html(Css.ContGugudanPrintStyle)          # 구구단 출력 용 CSS
            st.html(Css.ContKnncStyle)                  # k-최근접이웃분류 용 CSS
            # --------------------------------------------------------------
            # Hello StreamLit 메인 로직
            # --------------------------------------------------------------
            self.Form.ExpPrintString()
            self.Form.ExpInputText()
            self.Form.ExpInputTextNum()
            self.Form.ExpMakeTabs()
            self.Form.ExpGugudan()
            self.Form.ExpKnnc()
            # --------------------------------------------------------------
        except Exception as Ex:
            print(traceback.format_exc())               # 예외 출력하기(예외처리)
# ══════════════════════════════════════════════════════════════════════════════════════════
# 응용 프로그램 호출
# ══════════════════════════════════════════════════════════════════════════════════════════
if __name__ == '__main__':
    try:
        Main().Start()
    except Exception as Ex:
        print(traceback.format_exc())                   # 예외 출력하기(예외처리)
